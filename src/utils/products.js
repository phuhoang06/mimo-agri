// Hàm tìm kiếm sản phẩm
export function searchProducts(keyword, limit = 50) {
  if (!keyword || keyword.trim() === '') {
    return allProducts.slice(0, limit);
  }

  const searchTerm = keyword.toLowerCase().trim();
  const productScores = {};

  // Duyệt qua từng sản phẩm và tính điểm phù hợp
  allProducts.forEach(product => {
    let score = 0;

    // Tìm kiếm trong tiêu đề
    if (product.title.toLowerCase().includes(searchTerm)) {
      score += 10;
    }

    // Tìm kiếm trong mô tả
    if (product.description.toLowerCase().includes(searchTerm)) {
      score += 5;
    }

    // Tìm kiếm trong tags
    if (product.tags) {
      product.tags.forEach(tag => {
        if (tag.toLowerCase().includes(searchTerm)) {
          score += 3;
        }
      });
    }

    // Tìm kiếm trong danh mục
    if (product.categoryId.toLowerCase().includes(searchTerm)) {
      score += 2;
    }

    // Tìm kiếm trong mô tả chi tiết
    if (product.detailedDescription && product.detailedDescription.toLowerCase().includes(searchTerm)) {
      score += 1;
    }

    // Nếu sản phẩm bán chạy, tăng điểm
    if (topSellingProducts.some(p => p.id === product.id)) {
      score += 2;
    }

    // Nếu sản phẩm mới, tăng điểm
    if (newProducts.some(p => p.id === product.id)) {
      score += 2;
    }

    // Lưu điểm phù hợp nếu lớn hơn 0
    if (score > 0) {
      productScores[product.id] = { product, score };
    }
  });

  // Sắp xếp sản phẩm theo điểm phù hợp (cao đến thấp)
  const scoredProducts = Object.values(productScores)
    .sort((a, b) => b.score - a.score)
    .map(item => item.product);

  // Trả về số lượng sản phẩm theo giới hạn
  return scoredProducts.slice(0, limit);
}

// Lọc sản phẩm theo khoảng giá
export const filterProductsByPrice = (products, minPrice, maxPrice) => {
  return products.filter(product =>
    (!minPrice || product.price >= minPrice) &&
    (!maxPrice || product.price <= maxPrice)
  );
};

// Sắp xếp sản phẩm theo các tiêu chí
export const sortProducts = (products, sortType) => {
  const productsCopy = [...products];

  switch (sortType) {
    case 'price-asc':
      return productsCopy.sort((a, b) => a.price - b.price);
    case 'price-desc':
      return productsCopy.sort((a, b) => b.price - a.price);
    case 'name-asc':
      return productsCopy.sort((a, b) => a.title.localeCompare(b.title));
    case 'name-desc':
      return productsCopy.sort((a, b) => b.title.localeCompare(a.title));
    case 'newest':
      return productsCopy.sort((a, b) => (a.isNew === b.isNew) ? 0 : a.isNew ? -1 : 1);
    case 'top-selling':
      return productsCopy.sort((a, b) => (a.isTopSelling === b.isTopSelling) ? 0 : a.isTopSelling ? -1 : 1);
    default:
      return productsCopy;
  }
};

// Loại bỏ sản phẩm trùng lặp theo ID
export const getUniqueProducts = (products) => {
  if (!products) return [];
  return Array.from(new Map(products.map(item => [item.id, item])).values());
};

/**
 * Hàm lấy các sản phẩm liên quan theo cùng danh mục
 * @param {Number} productId - ID của sản phẩm cần lấy sản phẩm liên quan
 * @param {Number} limit - Số lượng sản phẩm trả về
 * @returns {Array} - Danh sách sản phẩm liên quan
 */
export function getRelatedProducts(productId, limit = 4) {
  const product = getProductById(productId);
  
  if (!product) {
    return [];
  }

  const categoryId = product.categoryId;
  
  // Mảng chứa điểm số cho từng sản phẩm
  const relatedScores = [];
  
  // Tính toán điểm số cho mỗi sản phẩm
  allProducts.forEach(p => {
    // Bỏ qua sản phẩm hiện tại
    if (p.id === product.id) return;
    
    let score = 0;
    
    // Sản phẩm cùng danh mục được điểm cao nhất
    if (p.categoryId === categoryId) {
      score += 10;
    }
    
    // Sản phẩm có tags giống nhau
    if (product.tags && p.tags) {
      const commonTags = product.tags.filter(tag => p.tags.includes(tag));
      score += commonTags.length * 2;
    }
    
    // Sản phẩm mới hoặc bán chạy được ưu tiên nhẹ
    if (p.isNew) score += 1;
    if (p.isTopSelling) score += 1;
    
    if (score > 0) {
      relatedScores.push({ product: p, score });
    }
  });
  
  // Sắp xếp theo điểm số từ cao đến thấp và lấy theo limit
  return relatedScores
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map(item => item.product);
}

/**
 * Hàm lấy các sản phẩm tương tự theo giá và danh mục
 * @param {Number} productId - ID của sản phẩm cần lấy sản phẩm tương tự
 * @param {Number} limit - Số lượng sản phẩm trả về
 * @returns {Array} - Danh sách sản phẩm tương tự
 */
export function getSimilarProducts(productId, limit = 4) {
  const product = getProductById(productId);
  
  if (!product) {
    return [];
  }
  
  // Mảng chứa điểm số cho từng sản phẩm
  const similarScores = [];
  
  // Giá cao nhất và thấp nhất để tính % chênh lệch
  const maxPrice = Math.max(...allProducts.map(p => p.price));
  const minPrice = Math.min(...allProducts.map(p => p.price));
  const priceRange = maxPrice - minPrice;
  
  // Tính toán điểm số cho mỗi sản phẩm
  allProducts.forEach(p => {
    // Bỏ qua sản phẩm hiện tại
    if (p.id === product.id) {
      return;
    }
    
    let score = 0;
    
    // Tính độ tương đồng về giá
    const priceSimilarity = 1 - Math.abs(p.price - product.price) / priceRange;
    score += priceSimilarity * 5;
    
    // Sản phẩm cùng danh mục được điểm
    if (p.categoryId === product.categoryId) {
      score += 5;
    }
    
    // Sản phẩm mới hoặc bán chạy được điểm
    if ((p.isNew && product.isNew) || (p.isTopSelling && product.isTopSelling)) {
      score += 2;
    }
    
    if (score > 0) {
      similarScores.push({ product: p, score });
    }
  });
  
  // Sắp xếp theo điểm số từ cao đến thấp và lấy theo limit
  return similarScores
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map(item => item.product);
}

/**
 * Lọc sản phẩm theo danh mục
 * @param {Array} products - Danh sách sản phẩm cần lọc
 * @param {String} category - ID danh mục
 * @returns {Array} - Danh sách sản phẩm đã lọc
 */
export const filterProductsByCategory = (products, category) => {
  if (!category || category === 'all') {
    return products;
  }
  
  return products.filter(product => product.categoryId === category);
};

/**
 * Lọc sản phẩm theo từ khóa tìm kiếm
 * @param {Array} products - Danh sách sản phẩm cần lọc
 * @param {String} searchTerm - Từ khóa tìm kiếm
 * @returns {Array} - Danh sách sản phẩm đã lọc
 */
export const filterProductsBySearch = (products, searchTerm) => {
  if (!searchTerm || searchTerm.trim() === '') {
    return products;
  }
  
  const term = searchTerm.toLowerCase().trim();
  
  return products.filter(product => {
    return (
      (product.title && product.title.toLowerCase().includes(term)) ||
      (product.description && product.description.toLowerCase().includes(term)) ||
      (product.tags && product.tags.some(tag => tag.toLowerCase().includes(term)))
    );
  });
};